    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

          Connection con = null;
          PreparedStatement pstmt = null;
    try {
        
        DriverManager.registerDriver(new org.postgresql.Driver());
        String conninfo = "jdbc:postgresql://localhost:5432/postgres";
        
        con = DriverManager.getConnection(conninfo, "allii", "12345");
        con.setAutoCommit(false);
        
        String sql = "INSERT INTO hwa.invoices (i_id, amount, payment_status, p_id) VALUES (?, ?, ?, ?)";
        pstmt = con.prepareStatement(sql);
        
        pstmt.setInt(1, Integer.parseInt(jTextField1.getText().trim()));
        pstmt.setBigDecimal(2, new BigDecimal(jTextField2.getText().trim())); // For amount
        pstmt.setString(3, jTextField3.getText().trim()); 
        pstmt.setInt(4, Integer.parseInt(jTextField4.getText().trim()));

         pstmt.executeUpdate();
         con.commit();
        
         setVisible(false);
         new NewJFrame1().setVisible(true);
         
         JOptionPane.showMessageDialog(null, "Successfully!");
    } catch (Exception ex) {
         JOptionPane.showMessageDialog(null, "Error: " + ex.getMessage());
        if (con != null) {
            try {
                con.rollback(); // Rollback in case of exception
            } catch (SQLException exRollback) {
                exRollback.printStackTrace();
            }
        }
    } finally {
         try {
            if (pstmt != null && !pstmt.isClosed()) pstmt.close();
            if (con != null && !con.isClosed()) con.close();
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }
    }//GEN-LAST:event_jButton2ActionPerformed

    
    
    
    
    
    
    
