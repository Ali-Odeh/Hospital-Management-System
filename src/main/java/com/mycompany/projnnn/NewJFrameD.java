    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Save Doctor'
        
        
           Connection con = null;
           PreparedStatement pstmt = null;
    try {
        // Register the PostgreSQL JDBC driver
        DriverManager.registerDriver(new org.postgresql.Driver());
        String conninfo = "jdbc:postgresql://localhost:5432/postgres";
        
        // Establish the database connection
        con = DriverManager.getConnection(conninfo, "allii", "12345");
        con.setAutoCommit(false);
        
        // Prepare the SQL statement using placeholders for parameters
        String sql = "INSERT INTO hwa.doctors (d_id, first_name, last_name, specialist, mobile, email, password, address) VALUES (?, ?, ?, ?, ?, ?, ?, ?)";
        pstmt = con.prepareStatement(sql);
        
        // Set the parameters for the prepared statement from the text fields and password field
        pstmt.setInt(1, Integer.parseInt(jTextField2.getText().trim())); // For doctor_id
        pstmt.setString(2, jTextField3.getText().trim()); // For first_name
        pstmt.setString(3, jTextField4.getText().trim()); // For last_name
        pstmt.setString(4, jTextField5.getText().trim()); // For specialist
        pstmt.setString(5, jTextField6.getText().trim()); // For mobile
        pstmt.setString(6, jTextField7.getText().trim()); // For email
        pstmt.setString(7, new String(jPasswordField1.getPassword())); // For password
        pstmt.setString(8, jTextField8.getText().trim()); // For address

        // Execute the update
        pstmt.executeUpdate();
      
        // Commit the transaction
        con.commit();
        
        // Close the statement and connection
        pstmt.close();
        con.close();
        
        // Hide the current window and show the new appointment frame
        setVisible(false);
        new NewJFrame1().setVisible(true);  
        
        // Display the success message
        JOptionPane.showMessageDialog(null, "Successfully!");
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(null, "Error: " + ex.toString());
        if (con != null) {
            try {
                con.rollback(); // Rollback in case of exception
            } catch (SQLException exRollback) {
                exRollback.printStackTrace();
            }
        }
    } finally {
                               // Ensure resources are closed in the 'finally' block to avoid resource leaks
        try {
            if (pstmt != null && !pstmt.isClosed()) pstmt.close();
            if (con != null && !con.isClosed()) con.close();
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

    
    
    
    
    
    
